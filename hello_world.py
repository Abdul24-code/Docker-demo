# List of animals
animals = ["zebra", "lion", "tiger", "elephant", "giraffe"]

# Sort the list in-place
animals.sort()
print("Sorted list (in-place):", animals)

# Alternatively, use sorted() to get a new sorted list
sorted_animals = sorted(["zebra", "lion", "tiger", "elephant", "giraffe"])
print("Sorted list (new list):", sorted_animals)

